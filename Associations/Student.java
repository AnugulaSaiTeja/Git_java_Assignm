package Associations;

public class Student {
	String name,city;
	int rollno;
	public void setname(String n)
	{
		name=n;
	}
	
	public void setcity(String c)
	{
		city=c;
	}
	
	public void setrollno(int n)
	{
		rollno=n;;
	}
	
	public String getname()
	{
		return name;
	}
	
	public String getcity()
	{
		return city;
	}
	
	public int getrollno()
	{
		return rollno;
	}

}
