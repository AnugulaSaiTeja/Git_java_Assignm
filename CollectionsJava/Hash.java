package com.CollectionsJava;
import java.util.*;
public class Hash {
	public static void main(String args[])
	{
		HashSet<String> s=new HashSet<String>();
		HashSet<Integer> i=new HashSet<Integer>();
		s.add("this");
		s.add("is");
		s.add("example");
		s.add("for");
		s.add("hashset");
		i.add(5);
		i.add(8);
		i.add(4);
		i.add(7);
		i.add(15);
		System.out.println(s);
		System.out.println(i);
		
	}

}
